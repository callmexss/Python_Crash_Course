age = 3


if age <= 2:
	print('baby')
elif age > 2 and age <= 4:
	print('hard walking')
elif age > 4 and age <= 13:
	print('child')
elif age > 13 and age <= 20:
	print('teen')
elif age > 20 and age <= 65:
	print('adult')
else:
	print('old')